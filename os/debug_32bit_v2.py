#!/usr/bin/env python3
"""DNAOS v3.4 — 32位最小化调试 v2"""
import struct, os

OUT = "/mnt/agents/output/dnaos2/os/out/debug_32bit_v2.img"
os.makedirs(os.path.dirname(OUT), exist_ok=True)

def w16(b, p, v):
    b[p] = v & 0xFF
    b[p+1] = (v >> 8) & 0xFF
    return p + 2

def w32(b, p, v):
    for i in range(4):
        b[p+i] = (v >> (i*8)) & 0xFF
    return p + 4

def build_mbr():
    m = bytearray(512)
    p = 0x40
    # segments + stack
    m[p:p+7] = bytes([0x31, 0xC0, 0x8E, 0xD8, 0x8E, 0xD0, 0xBC]); p += 7
    p = w16(m, p, 0x7C00)
    # "1\r\n"
    m[p:p+6] = bytes([0xB0, 0x31, 0xBA, 0xF8, 0x03, 0xEE]); p += 6
    m[p:p+4] = bytes([0xB0, 0x0D, 0xEE, 0xB0, 0x0A, 0xEE]); p += 4
    # INT13h read 128 sectors -> 0x1000:0x0000
    m[p:p+10] = bytes([0xB4, 0x02, 0xB0, 0x80, 0xB5, 0x00, 0xB1, 0x02, 0xB6, 0x00]); p += 10
    m[p:p+2] = bytes([0x8A, 0x16]); p = w16(m, p, 0x7DF8)
    m[p:p+2] = bytes([0xBB]); p = w16(m, p, 0x1000)
    m[p:p+6] = bytes([0x8E, 0xC3, 0x31, 0xDB, 0xCD, 0x13]); p += 6
    m[p:p+2] = bytes([0x72, 0x06]); p += 2
    m[p:p+5] = bytes([0xEA, 0x00, 0x00, 0x00, 0x10]); p += 5
    # error
    m[p:p+8] = bytes([0xB0, 0x45, 0xBA, 0xF8, 0x03, 0xEE, 0xEB, 0xFE]); p += 8
    assert p < 510, f"MBR overflow: {p}"
    m[510] = 0x55
    m[511] = 0xAA
    return bytes(m)

def build_kernel():
    k = bytearray(64 * 1024)

    # 16-bit @ 0x0000 -> loaded to 0x10000
    p = 0
    # A20 + CLI
    k[p:p+7] = bytes([0xE4, 0x92, 0x0C, 0x02, 0xE6, 0x92, 0xFA]); p += 7

    # GDT @ 0x80
    GDT = 0x80

    # DS=0x1000 then lgdt [0x80]
    k[p:p+5] = bytes([0xB8, 0x00, 0x10, 0x8E, 0xD8]); p += 5
    k[p:p+5] = bytes([0x0F, 0x01, 0x16, 0x80, 0x00]); p += 5
    # DS=0
    k[p:p+5] = bytes([0xB8, 0x00, 0x00, 0x8E, 0xD8]); p += 5

    # CR0.PE=1
    k[p:p+8] = bytes([0x0F, 0x20, 0xC0, 0x0C, 0x01, 0x0F, 0x22, 0xC0]); p += 8

    # GDT descriptors
    for i in range(8):
        k[GDT + 6 + i] = 0
    C32 = GDT + 14
    k[C32:C32+8] = bytes([0xFF, 0xFF, 0x00, 0x00, 0x00, 0x9A, 0xCF, 0x00])
    D = GDT + 22
    k[D:D+8] = bytes([0xFF, 0xFF, 0x00, 0x00, 0x00, 0x92, 0xCF, 0x00])

    # GDTR at file offset 0x80: limit=23, base=0x10080
    k[GDT+0] = 23
    k[GDT+1] = 0
    k[GDT+2] = 0x80
    k[GDT+3] = 0x00
    k[GDT+4] = 0x01
    k[GDT+5] = 0x00

    # far jmp 0x08:0x10200
    P32 = 0x200
    k[p:p+2] = bytes([0x66, 0xEA]); p += 2
    p = w32(k, p, 0x10000 + P32)
    p = w16(k, p, 0x08)

    # 32-bit @ 0x200 -> loaded to 0x10200
    p = P32
    # '2'
    k[p:p+7] = bytes([0xB0, 0x32, 0x66, 0xBA, 0xF8, 0x03, 0xEE]); p += 7
    k[p:p+6] = bytes([0xB0, 0x0D, 0xEE, 0xB0, 0x0A, 0xEE]); p += 6
    # load data segments
    k[p:p+10] = bytes([0x66, 0xB8, 0x10, 0x00, 0x8E, 0xD8, 0x8E, 0xC0, 0x8E, 0xD0]); p += 10
    # stack
    k[p:p+1] = bytes([0xBC]); p = w32(k, p, 0x00090000)
    # '3'
    k[p:p+7] = bytes([0xB0, 0x33, 0x66, 0xBA, 0xF8, 0x03, 0xEE]); p += 7
    k[p:p+6] = bytes([0xB0, 0x0D, 0xEE, 0xB0, 0x0A, 0xEE]); p += 6
    # halt
    k[p:p+2] = bytes([0xEB, 0xFE])

    return bytes(k)

mbr = build_mbr()
kernel = build_kernel()
disk = bytearray(64 * 1024 * 1024)
disk[0:512] = mbr
disk[512:512+len(kernel)] = kernel
with open(OUT, 'wb') as f:
    f.write(disk)
print(f"Built: {OUT}")
print("Expected: MBR '1' -> farjmp -> '2' -> '3' -> halt")
