""", base=0x3E)

    mbr[0x3E:0x3E+len(code)] = code
    mbr[510] = 0x55
    mbr[511] = 0xAA
    return mbr


def build_kernel(a):
    kernel = bytearray(64 * 1024)

    # GDT
    gdt = bytearray(24)
    struct.pack_into('<I', gdt, 8, 0x0000FFFF)
    struct.pack_into('<I', gdt, 12, 0x00CF9A00)
    struct.pack_into('<I', gdt, 16, 0x0000FFFF)
    struct.pack_into('<I', gdt, 20, 0x00CF9200)
    kernel[0x0000:0x0000+len(gdt)] = gdt
    struct.pack_into('<H', kernel, 0x0018, 23)
    struct.pack_into('<I', kernel, 0x001A, 0x10000)

    # 16-bit entry
    code16 = a.a16("""
